                    ),
                  ),
                ],
              ),
            ),
            const SizedBox(height: 30),
            
            // Quick Actions
            const Text(
              'Quick Actions',
              style: TextStyle(
                fontSize: 20,
                fontWeight: FontWeight.bold,
                color: Colors.black87,
              ),
            ),
            const SizedBox(height: 15),
            
            Row(
              children: [
                Expanded(
                  child: _ActionCard(
                    icon: Icons.camera_alt,
                    title: 'New Scan',
                    subtitle: 'Take a photo',
                    color: const Color(AppConstants.primaryColorValue),
                    onTap: () {
                      Navigator.of(context).push(
                        MaterialPageRoute(builder: (context) => const CameraScreen()),
                      );
                    },
                  ),
                ),
                const SizedBox(width: 15),
                Expanded(
                  child: _ActionCard(
                    icon: Icons.photo_library,
                    title: 'From Gallery',
                    subtitle: 'Choose image',
                    color: const Color(AppConstants.accentColorValue),
                    onTap: () async {
                      try {
                        final ImagePicker picker = ImagePicker();
                        final XFile? picked = await picker.pickImage(source: ImageSource.gallery);
                        if (picked == null) return;
                        if (kIsWeb) {
                          final bytes = await picked.readAsBytes();
                          // ignore: use_build_context_synchronously
                          Navigator.of(context).push(
                            MaterialPageRoute(
                              builder: (context) => ResultScreen(imageBytes: bytes),
                            ),
                          );
                        } else {
                          // ignore: use_build_context_synchronously
                          Navigator.of(context).push(
                            MaterialPageRoute(
                              builder: (context) => ResultScreen(imagePath: picked.path),
                            ),
                          );
                        }
                      } catch (e) {
                        // ignore: use_build_context_synchronously
                        ScaffoldMessenger.of(context).showSnackBar(
                          SnackBar(content: Text('Failed to pick image: $e')),
                        );
                      }
                    },
                  ),
                ),
              ],
            ),
            
            const SizedBox(height: 30),
            
            // Information Cards
            const Text(
              'Health Information',
              style: TextStyle(
                fontSize: 20,
                fontWeight: FontWeight.bold,
                color: Colors.black87,
              ),
            ),
            const SizedBox(height: 15),
            
            _InfoCard(
              icon: Icons.info_outline,
              title: 'About Skin Cancer',
              subtitle: 'Learn about early signs and prevention',
              onTap: () {
                // TODO: Navigate to info screen
              },
            ),
            const SizedBox(height: 10),
            _InfoCard(
              icon: Icons.tips_and_updates,
              title: 'Prevention Tips',
              subtitle: 'Daily habits for healthy skin',
              onTap: () {
                // TODO: Navigate to tips screen
              },
            ),
          ],
        ),
      ),
    );
  }
}

